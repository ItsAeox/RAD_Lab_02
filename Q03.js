function area(){
    let r = prompt("Enter the radius")
    var pi = 3.14
    var area = pi*r*r

    document.write("Area of the circle is ",area)
}
function peri(){
    let r = prompt("Enter the radius")
    var pi = 3.14
    var perimetre = 2*pi*r

    document.write("Perimetre of the circle is ",perimetre)

}
function volume(){
    let len = prompt("Enter the length of a side")
    var vol = len*len*len
    document.write("Volume of the cube is ",vol)

}
